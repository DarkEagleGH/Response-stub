package ua.com.abank.responsestub.config;

public class Mapping {

    private String path;
    private int code;
    private String data;

    public String getPath() {
        return path;
    }

    void setPath(String path) {
        this.path = path;
    }

    public int getCode() {
        return code;
    }

    void setCode(int code) {
        this.code = code;
    }

    public String getData() {
        return data;
    }

    void setData(String data) {
        this.data = data;
    }
}
